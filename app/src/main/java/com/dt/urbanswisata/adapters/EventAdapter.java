package com.dt.urbanswisata.adapters;

public class EventAdapter {

    private String eventName, eventDescription, eventWebsite, eventStart, eventEnd, eventLatitude, eventLongitude, eventPictures, eventGuest;
    private Float eventPrice;

    public String getEventName() {
        return eventName;
    }

    public void setEventName(String eventName) {
        this.eventName = eventName;
    }

    public String getEventDescription() {
        return eventDescription;
    }

    public void setEventDescription(String eventDescription) {
        this.eventDescription = eventDescription;
    }

    public String getEventWebsite() {
        return eventWebsite;
    }

    public void setEventWebsite(String eventWebsite) {
        this.eventWebsite = eventWebsite;
    }

    public String getEventStart() {
        return eventStart;
    }

    public void setEventStart(String eventStart) {
        this.eventStart = eventStart;
    }

    public String getEventEnd() {
        return eventEnd;
    }

    public void setEventEnd(String eventEnd) {
        this.eventEnd = eventEnd;
    }

    public String getEventLatitude() {
        return eventLatitude;
    }

    public void setEventLatitude(String eventLatitude) {
        this.eventLatitude = eventLatitude;
    }

    public String getEventLongitude() {
        return eventLongitude;
    }

    public void setEventLongitude(String eventLongitude) {
        this.eventLongitude = eventLongitude;
    }

    public String getEventPictures() {
        return eventPictures;
    }

    public void setEventPictures(String eventPictures) {
        this.eventPictures = eventPictures;
    }

    public String getEventGuest() {
        return eventGuest;
    }

    public void setEventGuest(String eventGuest) {
        this.eventGuest = eventGuest;
    }

    public Float getEventPrice() {
        return eventPrice;
    }

    public void setEventPrice(Float eventPrice) {
        this.eventPrice = eventPrice;
    }
}
